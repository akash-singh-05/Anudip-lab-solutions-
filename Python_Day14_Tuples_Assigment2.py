'''Write a Python program to convert a list to a tuple.
 Input: listx = [5, 10, 7, 4, 15, 3] Output: (5, 10, 7, 4, 15, 3)'''

# Define the list
listx = [5, 10, 7, 4, 15, 3]

# Convert the list to a tuple using tuple() function
tuplex = tuple(listx)

# Print the tuple
print("Converted tuple:", tuplex)

#Output:-Converted tuple: (5, 10, 7, 4, 15, 3)
